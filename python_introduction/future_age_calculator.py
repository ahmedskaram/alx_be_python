# Get the user's current age
current_age = int(input('How old are you? '))

# Define the current and future year
current_year = 2023
future_year = 2050

# A simpler way to calculate the future age
future_age = current_age + (future_year - current_year)

print(f'In 2050, you will be {future_age} years old')


